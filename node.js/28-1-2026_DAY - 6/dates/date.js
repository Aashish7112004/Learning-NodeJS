let date = new Date();

//set date  
date.setDate(15);
console.log("Set Date: " + date);


//set month
date.setMonth(5); 
console.log("Set Month: " + date);

//set full year
date.setFullYear(2025);
console.log("Set Full Year: " + date);

//set hours
date.setHours(10);
console.log("Set Hours: " + date);

//set minutes
date.setMinutes(30);
console.log("Set Minutes: " + date);

//set seconds
date.setSeconds(45);
console.log("Set Seconds: " + date);

//set milliseconds
date.setMilliseconds(500);
console.log("Set Milliseconds: " + date);

//set time
date.setTime(7200000); 
console.log("Set Time (in ms since Jan 1, 1970): " + date);




