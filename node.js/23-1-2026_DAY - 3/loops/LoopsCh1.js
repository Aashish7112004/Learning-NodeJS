//For Loop
console.log("For Loop Example:")
for(let i = 1; i <= 5; i++){
    console.log("Iteration number: " + i)
}
console.log(" ")

//While Loop
console.log("While Loop Example:")
let i = 1;
while(i <= 5){
    console.log("Iteration number: " + i)
    i++;
}
console.log(" ")

//Do While Loop
console.log("Do...While Loop Example:")
let j = 1;  
do{
    console.log("Iteration number: " + j)
    j++;

}while(j <= 5); 